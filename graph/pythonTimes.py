def prom(array): #Funcion de promedio
    row = len(array)
    promTimes = []
    for i in range(row):
        promT = sum(array[i])/15
        promTimes.append(promT)
    return promTimes

def devStand(array):
    row = len(array)
    devStd = []
    for i in range(row):
        devStd.append(np.std(array[i]))
    return(devStd)

